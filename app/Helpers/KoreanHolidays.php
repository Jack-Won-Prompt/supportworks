     * 특정 연도의 모든 공휴일 반환
     * @return array<string, string>  ['Y-m-d' => '휴일명']
     */
    public static function getHolidays(int $year): array
    {
        $holidays = [];

        // 고정 공휴일
        foreach (self::$fixed as $md => $name) {
            $holidays["$year-$md"] = $name;
        }

        // 변동 공휴일
        foreach (self::$variable[$year] ?? [] as $md => $name) {
            $holidays["$year-$md"] = $name;
        }

        // 대체공휴일: 고정 공휴일이 일요일에 해당하면 다음 월요일 추가
        foreach (self::$substituteEligible as $md) {
            $dt = Carbon::createFromFormat('Y-m-d', "$year-$md");
            if ($dt->isSunday()) {
                $sub = $dt->copy()->addDay()->format('Y-m-d');
                if (!isset($holidays[$sub])) {
                    $holidays[$sub] = '대체공휴일';
                }
            }
        }

        ksort($holidays);
        return $holidays;
    }
